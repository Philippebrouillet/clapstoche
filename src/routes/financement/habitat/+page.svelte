	<h1 class="text-3xl lg:text-5xl font-bold">Financement habitat : Les aides au logement</h1>
	<section>
		<h2 class="text-2xl lg:text-4xl font-bold mb-4">Les aides financières au logement</h2>
		<p class="text-lg leading-relaxed">
			Les aides financières au logement ont pour but de réduire la dépense de logement. Les seniors
			peuvent percevoir cette aide lorsqu’ils vivent à leur domicile ou en établissement, soit :
		</p>
		<ul class="list-disc list-inside mt-4 text-lg leading-relaxed">
			<li>
				<strong>L’APL (aide personnalisée au logement) :</strong> versée uniquement si le logement
				ou l’établissement sont conventionnés, c’est-à-dire qu’une convention avec le Préfet a été
				signée par le bailleur ou l’établissement. Pour obtenir l’information sur le
				conventionnement APL des EHPAD <a href="#" class="text-blue-800 underline">cliquez ici</a>.
			</li>
			<li><strong>L’ALS (allocation de logement sociale) :</strong> versée dans les autres cas.</li>
		</ul>
		<p class="mt-2 text-red-500 font-semibold">ATTENTION ! APA & PCH ne sont pas cumulables.</p>
	</section>

	<section>
		<h3 class="text-xl lg:text-3xl font-bold mb-4">Conditions</h3>
		<p class="text-lg leading-relaxed">
			Une aide au logement peut être versée à condition d’une part que votre logement soit votre
			résidence principale, et d’autre part, d’être :
		</p>
		<ul class="list-disc list-inside mt-4 text-lg leading-relaxed">
			<li>
				Locataire ou colocataire ou sous-locataire déclaré ou propriétaire d’un logement meublé ou
				non.
			</li>
			<li>Accédant à la propriété ayant bénéficié d’un prêt pour l’achat d’un logement.</li>
			<li>Résident en EPHAD (même si vous gardez votre ancien logement).</li>
			<li>En hébergement familial.</li>
			<li>En situation régulière sur le territoire français.</li>
			<li>Pas de limite d’âge.</li>
		</ul>
	</section>

	<section>
		<h3 class="text-xl lg:text-3xl font-bold mb-4">Faire une demande</h3>
		<p class="text-lg leading-relaxed">
			La demande se fait auprès de la CAF (caisse d’allocations familiales) ou de la MSA (mutualité
			sociale agricole) quand on relève de ce régime de protection sociale.
		</p>
	</section>

	<section>
		<h3 class="text-xl lg:text-3xl font-bold mb-4">Calcul de l’aide</h3>
		<p class="text-lg leading-relaxed">Le calcul tient compte des paramètres suivants :</p>
		<ul class="list-disc list-inside mt-4 text-lg leading-relaxed">
			<li>Situation géographique du logement ou de l’EPHAD.</li>
			<li>
				Les ressources du bénéficiaire et de son conjoint. Pour les résidents en EPHAD, seules les
				ressources du résident sont prises en compte.
			</li>
			<li>Le montant du loyer, du prêt, ou du coût de l’hébergement.</li>
		</ul>
	</section>

	<section>
		<h3 class="text-xl lg:text-3xl font-bold mb-4">Versement de l’aide</h3>
		<p class="text-lg leading-relaxed">
			L’aide au logement est versée tous les mois. Dans le cas où elle est versée directement au
			bailleur, au prêteur ou à l’établissement, le montant facturé sera diminué du montant de cette
			aide.
		</p>
		<p class="text-lg leading-relaxed mt-4">
			Pour faire l’estimation des aides au logement à domicile, la CAF met à votre disposition une
			simulation sur son site <a href="https://www.caf.fr" class="text-blue-800 underline">ici</a>.
		</p>
	</section>
