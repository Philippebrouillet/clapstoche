	</div>
	<section>
		<h2 class="text-2xl lg:text-4xl font-bold mb-4">Présentation de l'APA</h2>
		<p class="text-lg leading-relaxed">
			L’APA (Allocation Personnalisée d’Autonomie) concerne les personnes âgées de plus de 60 ans,
			nécessitant une aide au quotidien, soit en raison d’une perte d’autonomie ponctuelle ou
			durable, soit parce que leur état de santé nécessite une surveillance régulière.
		</p>
		<ul class="list-disc list-inside mt-4 text-lg leading-relaxed">
			<li>
				<strong>A domicile :</strong> l’APA permet de financer une partie des dépenses nécessaires au
				maintien à domicile.
			</li>
			<li>
				<strong>En établissement :</strong> l’APA couvre une partie du tarif dépendance fixé par l’établissement
				médico-social d’accueil (par exemple un EHPAD).
			</li>
		</ul>
	</section>

	<section>
		<h2 class="text-2xl lg:text-4xl font-bold mb-4">Conditions d'éligibilité à domicile</h2>
		<ul class="list-disc list-inside mt-4 text-lg leading-relaxed">
			<li>Vous devez être âgé d’au moins 60 ans.</li>
			<li>
				Vous devez, du fait de votre dépendance, être rattaché à l’un des groupes 1 à 4 de la grille
				AGGIR (Guide AGGIR).
			</li>
			<li>Vous devez habiter en France de manière stable et régulière, soit :</li>
			<ul class="list-disc list-inside ml-8">
				<li>à votre domicile,</li>
				<li>au domicile d’un proche qui vous héberge,</li>
				<li>chez un accueillant familial,</li>
				<li>dans un établissement dont la capacité d’accueil est inférieure à 25 places,</li>
				<li>dans une résidence autonomie (anciennement « foyer-logement »).</li>
			</ul>
		</ul>
	</section>

	<section>
		<h2 class="text-2xl lg:text-4xl font-bold mb-4">Comment bénéficier de l’APA ?</h2>
		<ol class="list-decimal list-inside mt-4 text-lg leading-relaxed space-y-4">
			<li>
				<p>
					La première étape consiste à télécharger un dossier de demande d’APA <a
						href="#"
						class="text-blue-800 underline">(cliquez ici)</a
					> ou à le retirer auprès de votre mairie (CCAS), du Conseil départemental ou d’un point d’information
					local :
				</p>
				<ul class="list-disc list-inside ml-8 mt-2">
					<li>
						Centre Local d’Information et de Coordination (CLIC) de votre secteur destiné aux
						personnes âgées ou à leurs proches.
					</li>
					<li>
						Plateforme d’accompagnement et de répit, pour répondre aux besoins des aidants, dont
						vous trouverez la liste en <a href="#" class="text-blue-800 underline"
							>cliquant sur ce lien</a
						>.
					</li>
				</ul>
			</li>
			<li>
				<p>
					La deuxième étape consiste à remplir et renvoyer le dossier à l’adresse signalée sur
					celui-ci, accompagné des pièces suivantes :
				</p>
				<ul class="list-disc list-inside ml-8 mt-2">
					<li>
						Photocopie de votre dernier avis d’imposition de taxe foncière sur les propriétés bâties
						et non bâties (si vous êtes propriétaire).
					</li>
					<li>
						Photocopie du livret de famille, de votre carte d’identité, de votre passeport ou d’un
						extrait d’acte de naissance ou photocopie de votre carte de résident ou titre de séjour
						(si vous êtes étranger non européen).
					</li>
					<li>RIB – Relevé d’identité bancaire ou postal.</li>
					<li>
						Éventuellement un certificat médical, un justificatif d’adresse ou d’élection de
						domicile.
					</li>
				</ul>
				<p class="mt-2">
					Les services du département sont tenus d’accuser réception de votre dossier dans les 10
					jours dans la mesure où votre dossier est complet.
				</p>
			</li>
			<li>
				<p>
					Une équipe médico-sociale effectuera une visite à votre domicile puis évaluera votre degré
					de perte d’autonomie, ainsi que le besoin de répit de vos proches aidants.
				</p>
			</li>
			<li>
				<p>Décision d’attribution :</p>
				<ul class="list-disc list-inside ml-8 mt-2">
					<li>
						Si votre degré de perte d’autonomie ne justifie pas l’établissement d’un plan d’aide,
						vous ne pouvez pas bénéficier de l’APA à domicile.
					</li>
					<li>
						Dans le cas contraire, si vous êtes classé dans l’un des groupes 1 à 4 de la grille
						AGGIR, un plan d’aide vous est proposé, en fonction des dépenses nécessaires pour votre
						maintien à domicile. Le plan d’aide peut prévoir, par exemple :
					</li>
					<ul class="list-disc list-inside ml-8 mt-2">
						<li>la rémunération d’une aide à domicile ou d’un accueillant familial,</li>
						<li>des aides concernant le Portage de repas,</li>
						<li>des aides liées au transport,</li>
						<li>des aides techniques et des mesures d’adaptation du logement,</li>
						<li>
							le recours à un ou des dispositifs de répit : accueil temporaire (en établissement ou
							famille d’accueil), relais à domicile,
						</li>
					</ul>
					<li>
						Après acceptation du plan d’aide, l’attribution de l’APA est accordée par le
						département. Cette décision doit intervenir dans les 2 mois suivant la date de réception
						du dossier complet de demande. Passé ce délai, un montant forfaitaire vous est versé
						dans l’attente d’une décision explicite, etc.
					</li>
					<li>
						Une évaluation des besoins de vos proches aidants est également proposée par l’équipe
						médico-sociale.
					</li>
				</ul>
			</li>
			<li>
				<p>
					La cinquième étape consiste à accepter ou demander des modifications du plan d’aide
					proposé.
				</p>
			</li>
			<li>
				<p>Versement de l’allocation personnalisée d’autonomie :</p>
				<ul class="list-disc list-inside ml-8 mt-2">
					<li>
						Elle peut être versée à son bénéficiaire : la partie servant à payer des aides
						régulières est versée mensuellement, au plus tard le 10 du mois auquel elle se rapporte.
						Le 1er versement intervient le mois qui suit celui de la décision d’attribution, il
						comprend le versement de l’APA due à partir de la date d’ouverture des droits.
					</li>
					<li>
						Une partie de l’APA peut être versée directement au service d’aide à domicile choisi par
						le bénéficiaire.
					</li>
					<li>
						Une partie de l’APA peut être versée directement à la personne ou à l’organisme qui
						fournit l’aide technique, réalise l’aménagement du logement ou assure l’accueil
						temporaire ou le répit à domicile.
					</li>
					<li>
						La partie de l’APA destinée à rémunérer un salarié employé à domicile, un accueillant
						familial ou un service d’aide à domicile agréé peut être versée sous forme de CESU
						pré-financé.
					</li>
				</ul>
			</li>
		</ol>
	</section>

