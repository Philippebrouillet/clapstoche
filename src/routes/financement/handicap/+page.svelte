<main class="max-w-7xl mx-auto px-4 sm:px-6 lg:px-8 space-y-12 py-20">
	<h1 class="text-3xl lg:text-5xl font-bold">Prestation de Compensation du Handicap (PCH)</h1>
	<section>
		<h2 class="text-2xl lg:text-4xl font-bold mb-4">
			Financement handicap : Prestation de compensation du Handicap (PCH)
		</h2>
		<p class="text-lg leading-relaxed">
			La PCH est destinée aux personnes qui ont besoin d’une aide dans la réalisation des actes de
			la vie quotidienne du fait d’un handicap. La prise en charge des dépenses liées au handicap
			peut être totale ou partielle, et concernent certaines aides. Son attribution dépend de
			plusieurs conditions.
		</p>
	</section>

	<section>
		<h3 class="text-xl lg:text-3xl font-bold mb-4">Aides prises en charge par la PCH</h3>
		<ul class="list-disc list-inside mt-4 text-lg leading-relaxed">
			<li>
				<strong>Les aides humaines :</strong> Tout prestataire ou aidant familial qui intervient pour
				l’accomplissement des actes essentiels de la vie quotidienne : toilette, alimentation, déplacements…
			</li>
			<li>
				Les aidants familiaux pouvant être dédommagés sont : le conjoint, le concubin, la personne
				avec laquelle un PACS a été conclu, l’ascendant (parents & grands-parents), le descendant
				(enfants & petits-enfants), les frères & sœurs.
			</li>
			<li>
				<strong>Les aides techniques :</strong> équipements conçus et adaptés pour pallier le handicap.
			</li>
			<li><strong>Les aides pour l’aménagement du logement</strong></li>
			<li>
				<strong
					>Les aides pour l’aménagement du véhicule ainsi que les surcoûts liés au transport.</strong
				>
			</li>
			<li>
				<strong>Les aides pour les dépenses permanentes liées au handicap :</strong> téléalarme, protections
				pour incontinence…
			</li>
			<li>
				<strong
					>Les aides pour certaines dépenses ponctuelles liées au handicap qui n’ouvrent pas droit à
					une prise en charge au titre d’un des autres éléments de la PCH</strong
				>
			</li>
			<li><strong>Les aides animalières</strong></li>
		</ul>
	</section>

	<section class="bg-red-100 border-l-4 border-red-500 p-4">
		<h3 class="text-xl lg:text-3xl font-bold mb-4 text-red-600">
			Aides non prises en charge par la PCH
		</h3>
		<ul class="list-disc list-inside mt-4 text-lg leading-relaxed text-red-600">
			<li>
				<strong>Accueil dans un établissement médico-social ou un établissement de santé.</strong>
			</li>
			<li>
				<strong>Aide à la vie domestique :</strong> ménage, courses, entretien du linge, préparation
				des repas… (possibilité de bénéficier pour cela d’une aide sociale versée par le conseil départemental
				et cumulable avec la PCH. Son attribution est soumise à conditions de ressources.
			</li>
		</ul>
	</section>
	<section>
		<h3 class="text-xl lg:text-3xl font-bold mb-4">Conditions d’accès</h3>
		<h4 class="text-lg lg:text-2xl font-bold mt-4">Les conditions de handicap</h4>
		<ul class="list-disc list-inside mt-4 text-lg leading-relaxed">
			<li>
				1 limitation absolue ou 2 limitations graves (handicap à la marche, la toilette, la vue,
				l’audition….)
			</li>
			<li>
				La MDH (maison départementale des personnes handicapées) procède à l’évaluation du handicap.
			</li>
			<li>
				La CDAPH (commission des droits et de l’autonomie des personnes handicapées) propose un plan
				de compensation.
			</li>
		</ul>

		<h4 class="text-lg lg:text-2xl font-bold mt-4">Les conditions d’âge</h4>
		<ul class="list-disc list-inside mt-4 text-lg leading-relaxed">
			<li>Être âgé de moins de 60 ans lors de la première demande.</li>
			<li>
				Être âgé de moins de 75 ans et avoir rempli les conditions d’accès à la PCH avant 60 ans.
			</li>
			<li>
				Être encore en activité professionnelle, même au-delà de l’âge légal de départ à la
				retraite.
			</li>
			<li>
				Au-delà de 60 ans alors que l’on est retraité, l’aide relève principalement de l’APA
				(allocation personnalisée d’autonomie).
			</li>
			<li>
				A plus de 60 ans si on est éligible à la fois à l’APA et à la PCH, il faut choisir quelle
				aide est la plus intéressante en fonction de sa situation.
			</li>
		</ul>
		<p class="mt-2 text-red-500 font-semibold">ATTENTION ! APA & PCH ne sont pas cumulables.</p>

		<h4 class="text-lg lg:text-2xl font-bold mt-4">Les conditions de ressources</h4>
		<p class="text-lg leading-relaxed mt-4">
			La PCH est une prestation universelle. Toute personne handicapée éligible à la PCH peut en
			bénéficier quels que soient ses revenus.
		</p>
		<ul class="list-disc list-inside mt-4 text-lg leading-relaxed">
			<li>
				A 100 % si les ressources annuelles de patrimoine de la personne handicapée sont inférieures
				ou égales à 26 500,42 € par an (au 1er janvier 2017).
			</li>
			<li>A 80% si ses ressources sont supérieures.</li>
		</ul>
		<p class="text-lg leading-relaxed mt-4">
			Les conditions de résidence : Il faut résider en France de façon stable et régulière.
		</p>
	</section>

	<section>
		<h3 class="text-xl lg:text-3xl font-bold mb-4">Montant des aides</h3>
		<p class="text-lg leading-relaxed mt-4">
			Le montant des aides est plafonné, distinctement selon le type d’aide. Reportez vous ici pour
			connaître les barèmes précis ainsi que les plafonds applicables au 1er janvier 2017.
		</p>
		<h4 class="text-lg lg:text-2xl font-bold mt-4">Quelques exemples</h4>
		<ul class="list-disc list-inside mt-4 text-lg leading-relaxed">
			<li>Pour les aides techniques : 3 960 € maximum sur 3 ans.</li>
			<li>Pour l’aménagement du logement : 10 000 € sur 10 ans.</li>
			<li>Pour les charges spécifiques : 100 € par mois pendant 10 ans.</li>
			<li>
				Pour l’aidant familial 3,73 €/H. Ce tarif passe à 5,59 €/H pour l’aidant ayant réduit ou
				interrompu son activité professionnelle.
			</li>
		</ul>
	</section>

	<section>
		<h3 class="text-xl lg:text-3xl font-bold mb-4">Attribution de la PCH</h3>
		<p class="text-lg leading-relaxed mt-4">
			La PCH est attribuée par la CDAPH (commission des droits et de l’autonomie des personnes
			handicapées) de la MDPH (maison départementale des personnes handicapées)
		</p>
	</section>

	<section>
		<h3 class="text-xl lg:text-3xl font-bold mb-4">Versement de la PCH</h3>
		<p class="text-lg leading-relaxed mt-4">
			La PCH est versée par le conseil départemental. Les sommes versées ne sont pas récupérables.
		</p>
	</section>
